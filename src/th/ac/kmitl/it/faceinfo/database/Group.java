package th.ac.kmitl.it.faceinfo.database;

public class Group {
	private String group_name;
	private String group_id;
	private String group_detail;
	private String group_img;
	private String group_date_add;
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getGroup_detail() {
		return group_detail;
	}
	public void setGroup_detail(String group_detail) {
		this.group_detail = group_detail;
	}
	public String getGroup_img() {
		return group_img;
	}
	public void setGroup_img(String group_img) {
		this.group_img = group_img;
	}
	public String getGroup_time_add() {
		return group_date_add;
	}
	public void setGroup_date_add(String group_date_add) {
		this.group_date_add = group_date_add;
	}
	

}
